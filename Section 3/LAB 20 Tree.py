def get_root(graph):
    the_root = None
    # review all columns
    for c in range(len(graph)):
        # chek how many parents the node has
        number_of_parents = 0
        for r in range(len(graph)):
            number_of_parents += graph[r][c]
            # if the node hasn't parents, it is the root
        if number_of_parents == 0:
            the_root = c
            break
    # return found root or none
    return the_root


